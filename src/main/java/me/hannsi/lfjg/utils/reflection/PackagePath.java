package me.hannsi.lfjg.utils.reflection;

/**
 * Utility class for storing package paths used in the application.
 */
public class PackagePath {
    /**
     * Base package path.
     */
    public static final String base = "me.hannsi.lfjg";

    /**
     * Package path for frame settings.
     */
    public static final String frameSettings = base + ".frame.setting.settings";

    /**
     * Package path for managers.
     */
    public static final String managers = base + ".frame.manager.managers";
}